CHATGPT_LINK_RE = re.compile(r"원본 대화:\s*\[[^\]]*\]\((https?://[^)]+)\)")
CONV_KEY_RE = re.compile(r"chatgpt:c-([0-9a-fA-F-]+)")
FOLDER_ID_RE = re.compile(r"__c-([0-9a-fA-F-]+)")
FILENAME_RE = re.compile(r"^(\d{4}-\d{2}-\d{2})_(turn[0-9]+(?:-[0-9]+)?)_")
STATUS_WORDS = ("saved", "missing", "variant", "escape", "invalid")
PROJECT_MONTH_RE = re.compile(r"^\d{4}-\d{2}$")


# --------------------------------------------------------------------------- #
# Note model
# --------------------------------------------------------------------------- #
@dataclass
class Note:
    path: Path
    name: str                       # filename stem
    md_filename: str                # original *.md filename
    html_filename: str              # <stem>.html
    title: str
    date: str
    turn_range: str
    chatgpt_url: Optional[str]
    summary: str
    keywords: List[str]
    status_counts: Dict[str, int]
    body_md: str                    # markdown after the title line / 원본 대화 line
    raw: str


def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8-sig", errors="replace")


def strip_frontmatter(text: str) -> str:
    text = text.lstrip("﻿")
    if text.startswith("---"):
        m = re.match(r"^---\s*\n.*?\n---\s*\n?", text, flags=re.S)
        if m:
            return text[m.end():]
    return text


def parse_note(path: Path) -> Note:
    raw = read_text(path)
    body = strip_frontmatter(raw)
    lines = body.splitlines()

    # title = first H1
    title = path.stem
    title_idx = -1
    for i, line in enumerate(lines):
        if line.startswith("# "):
            title = line[2:].strip()
            title_idx = i
            break

    # chatgpt url: explicit "원본 대화" link, else reconstruct from conv key / folder
    url_match = CHATGPT_LINK_RE.search(body)
    chatgpt_url = url_match.group(1) if url_match else None
    if not chatgpt_url:
        m = CONV_KEY_RE.search(body) or FOLDER_ID_RE.search(body)
        if m:
            chatgpt_url = f"https://chatgpt.com/c/{m.group(1)}"

    summary = extract_section_text(lines, "선요약")
    kw_text = extract_section_text(lines, "검색 키워드")
    keywords = [k.strip() for k in re.split(r"[,\n]", kw_text) if k.strip()] if kw_text else []

    status_counts = parse_status_counts(body)
    date, turn_range = parse_filename_meta(path.name)
    if not turn_range:
        turn_range = guess_turn_range(body)

    # body for the detail page: drop the leading title line and the 원본 대화 line
    drop = set()
    if title_idx >= 0:
        drop.add(title_idx)
    for i, line in enumerate(lines):
        if line.strip().startswith("원본 대화:"):
            drop.add(i)
    body_md = "\n".join(l for i, l in enumerate(lines) if i not in drop).strip()

    return Note(
        path=path,
        name=path.stem,
        md_filename=path.name,
        html_filename=path.stem + ".html",
        title=title,
        date=date,
        turn_range=turn_range,
        chatgpt_url=chatgpt_url,
        summary=summary,
        keywords=keywords,
        status_counts=status_counts,
        body_md=body_md,
        raw=raw,
    )


def extract_section_text(lines: List[str], heading: str) -> str:
    """Return the text content directly under '## <heading>' until the next '## '."""
    out: List[str] = []
    collecting = False
    for line in lines:
        if re.match(r"^##\s+", line):
            if collecting:
                break
            collecting = re.sub(r"^##\s+", "", line).strip() == heading
            continue
        if collecting:
            out.append(line)
    return "\n".join(out).strip()


def parse_status_counts(body: str) -> Dict[str, int]:
    """Prefer an explicit 'status counts: saved n / missing n' line; else count the 파일 범위 table."""
    counts = {w: 0 for w in STATUS_WORDS}
    m = re.search(r"status counts:\s*(.+)", body)
    if m:
        for word in STATUS_WORDS:
            wm = re.search(rf"{word}\s+(\d+)", m.group(1))
            if wm:
                counts[word] = int(wm.group(1))
        if any(counts.values()):
            return {k: v for k, v in counts.items() if v}
    # fallback: count status column of the 파일 범위 table
    for line in body.splitlines():
        if not line.strip().startswith("|"):
            continue
        cells = [c.strip() for c in line.strip().strip("|").split("|")]
        if len(cells) >= 2:
            status = cells[1].lower()
            if status in counts:
                counts[status] += 1
    return {k: v for k, v in counts.items() if v}


def parse_filename_meta(filename: str) -> Tuple[str, str]:
    m = FILENAME_RE.match(filename)
    if m:
        return m.group(1), m.group(2)
    return "", ""


def guess_turn_range(body: str) -> str:
    m = re.search(r"turn range:\s*(turn[0-9-]+)", body)
    return m.group(1) if m else ""


# --------------------------------------------------------------------------- #
# Source snapshots (snapshots/**/*.md) -> snapshot_html drill-down pages
# --------------------------------------------------------------------------- #
@dataclass
class SnapshotDoc:
    path: Path
    rel: str            # path relative to snapshots/ (forward slashes, .md)
    html_rel: str       # same path with .html
    turn: Optional[int]
    status: str
    detected_at: str
    reason: str
    missing_sections: List[str]
    conv_url: Optional[str]
    conv_id: Optional[str]
    body_md: str


def conv_id_from(value: str) -> Optional[str]:
    s = str(value or "")
    m = re.search(r"/c/([0-9a-fA-F-]+)", s) or re.search(r"c-([0-9a-fA-F-]+)", s)
    return m.group(1) if m else None


def parse_frontmatter_map(text: str) -> Dict[str, object]:
    text = text.lstrip("﻿")
    if not text.startswith("---"):
        return {}
    m = re.match(r"^---\s*\n(.*?)\n---", text, flags=re.S)
    if not m:
        return {}
    meta: Dict[str, object] = {}
    current: Optional[str] = None
    for line in m.group(1).splitlines():
        kv = re.match(r"^([A-Za-z_][\w]*):\s*(.*)$", line)
        if kv:
            current = kv.group(1)
            meta[current] = kv.group(2).strip().strip('"').strip("'")
        elif current and line.strip().startswith("-"):
            meta.setdefault(current + "__list", []).append(
                line.strip()[1:].strip().strip('"').strip("'")
            )
    return meta


def parse_snapshot(path: Path, snapshots_dir: Path) -> SnapshotDoc:
    raw = read_text(path)
    meta = parse_frontmatter_map(raw)
    body = strip_frontmatter(raw)
    rel = path.relative_to(snapshots_dir).as_posix()
    html_rel = (rel[:-3] if rel.lower().endswith(".md") else rel) + ".html"

    turn: Optional[int] = None
    turn_meta = str(meta.get("turn") or "")
    if re.search(r"\d+", turn_meta):
        turn = int(re.search(r"\d+", turn_meta).group())
    else:
        fm = re.search(r"turn[_-]?(\d+)", path.name, flags=re.I)
        if fm:
            turn = int(fm.group(1))

    conv_url = str(meta.get("conversationUrl") or "") or None
    conv_id = conv_id_from(str(meta.get("conversationKey") or "") or conv_url or rel)
    if not conv_url and conv_id:
        conv_url = f"https://chatgpt.com/c/{conv_id}"

    return SnapshotDoc(
        path=path,
        rel=rel,
        html_rel=html_rel,
        turn=turn,
        status=str(meta.get("status") or "unknown").lower(),
        detected_at=str(meta.get("detectedAt") or ""),
        reason=str(meta.get("reason") or ""),
        missing_sections=list(meta.get("missingSections__list") or []),
        conv_url=conv_url,
        conv_id=conv_id,
        body_md=body.strip(),
    )


def arch_to_snap_href(html_rel: str) -> str:
    """Relative href from an archive_html/<note>.html page to a snapshot page."""
    return "../snapshot_html/" + html_rel


def inject_snapshot_links(body_md: str, turn_href: Dict[int, str], path_href: Dict[str, str]) -> str:
    """Add a '원문 보기' link in the 메모 cell of 파일 범위 rows and on 원문 근거 파일 paths."""
    out: List[str] = []
    for line in body_md.splitlines():
        new = line
        if line.lstrip().startswith("|"):
            cells = line.split("|")
            turn_txt = cells[1].strip() if len(cells) >= 6 else ""
            if turn_txt.isdigit() and int(turn_txt) in turn_href:
                href = turn_href[int(turn_txt)]
                memo = cells[-2].strip()
                cells[-2] = f" {memo + ' · ' if memo else ''}[원문 보기]({href}) "
                out.append("|".join(cells))
                continue
        pm = re.match(r"^(\s*[-*]\s*)`?(snapshots/[^\s`]+?\.md)`?\s*$", line)
        if pm and pm.group(2) in path_href:
            base = pm.group(2).split("/")[-1]
            new = f"{pm.group(1)}[{base} 열기]({path_href[pm.group(2)]})"
        out.append(new)
    return "\n".join(out)


