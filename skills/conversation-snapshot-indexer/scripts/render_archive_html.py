def main() -> None:
    ap = argparse.ArgumentParser(description="Render the snapshot archive to HTML (deterministic).")
    ap.add_argument("--archive-root", default=DEFAULT_ROOT, help="archive root containing archive/*.md")
    ap.add_argument("--no-readme", action="store_true", help="do not rewrite README.md")
    args = ap.parse_args()

    root = Path(args.archive_root).expanduser()
    archive_dir = root / "archive"
    if not archive_dir.is_dir():
        raise SystemExit(f"archive folder not found: {archive_dir}")

    md_files = sorted(p for p in archive_dir.glob("*.md") if p.name.lower() != "readme.md")
    if not md_files:
        raise SystemExit(f"no archive notes found in {archive_dir}")

    notes = [parse_note(p) for p in md_files]
    # newest first by date, then filename — stable & deterministic
    notes.sort(key=lambda n: (n.date, n.name), reverse=True)

    html_dir = root / "archive_html"
    html_dir.mkdir(exist_ok=True)

    # clean stale html for removed notes
    valid_html = {n.html_filename for n in notes}
    for stale in html_dir.glob("*.html"):
        if stale.name not in valid_html:
            stale.unlink()

    # source snapshots -> snapshot_html drill-down pages
    snapshots_dir = root / "snapshots"
    snaps: List[SnapshotDoc] = []
    if snapshots_dir.is_dir():
        snaps = [parse_snapshot(p, snapshots_dir) for p in sorted(snapshots_dir.rglob("*.md"))]

    note_by_conv: Dict[str, Note] = {}
    for note in notes:
        cid = conv_id_from(note.chatgpt_url or "")
        if cid and cid not in note_by_conv:
            note_by_conv[cid] = note

    snap_dir = root / "snapshot_html"
    if snaps:
        snap_dir.mkdir(exist_ok=True)
        valid_snaps = {s.html_rel for s in snaps}
        for stale in snap_dir.rglob("*.html"):
            if stale.relative_to(snap_dir).as_posix() not in valid_snaps:
                stale.unlink()
        for snap in snaps:
            target = snap_dir / snap.html_rel
            target.parent.mkdir(parents=True, exist_ok=True)
            target.write_text(
                render_snapshot_page(snap, note_by_conv.get(snap.conv_id)), encoding="utf-8"
            )

    # per-note maps: turn -> snapshot page, "snapshots/<rel>" -> snapshot page
    snaps_by_conv: Dict[str, List[SnapshotDoc]] = {}
    for snap in snaps:
        if snap.conv_id:
            snaps_by_conv.setdefault(snap.conv_id, []).append(snap)

    for note in notes:
        cid = conv_id_from(note.chatgpt_url or "")
        group = snaps_by_conv.get(cid or "", [])
        turn_href = {s.turn: arch_to_snap_href(s.html_rel) for s in group if s.turn is not None}
        path_href = {"snapshots/" + s.rel: arch_to_snap_href(s.html_rel) for s in group}
        (html_dir / note.html_filename).write_text(
            render_detail(note, turn_href, path_href), encoding="utf-8"
        )

    pending_project_jobs = count_project_snapshot_jobs(project_root_from_script())
    generated_at = datetime.now().astimezone()
    generated_iso = generated_at.isoformat(timespec="minutes")
    generated_label = generated_at.strftime("%Y.%m.%d")
    (root / "index.html").write_text(
        render_index(notes, pending_project_jobs, generated_iso, generated_label),
        encoding="utf-8",
    )

    readme_changed = False
    if not args.no_readme:
        readme_changed = update_readme(root / "README.md", notes)

    print(f"notes rendered : {len(notes)}")
    print(f"index.html     : {root / 'index.html'}")
    print(f"archive_html/  : {html_dir} ({len(notes)} files)")
    print(f"snapshot_html/ : {root / 'snapshot_html'} ({len(snaps)} files)")
    print(f"project jobs   : {pending_project_jobs}")
    print(f"README.md      : {'updated' if readme_changed else 'unchanged'}")


if __name__ == "__main__":
    main()
