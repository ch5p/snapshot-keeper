def build_readme_section(notes: List[Note]) -> str:
    lines = [README_SECTION, "", "- 빠른 탐색: [index.html](index.html)", ""]
    for note in notes:
        lines.append(f"- [{note.md_filename}](archive/{note.md_filename})")
    return "\n".join(lines) + "\n"


def update_readme(readme_path: Path, notes: List[Note]) -> bool:
    if not readme_path.exists():
        return False
    raw = readme_path.read_bytes()
    has_bom = raw.startswith(b"\xef\xbb\xbf")
    text = raw.decode("utf-8-sig", errors="replace")
    section = build_readme_section(notes)

    if README_SECTION in text:
        pattern = re.compile(
            re.escape(README_SECTION) + r".*?(?=\n## |\Z)", flags=re.S
        )
        new_text = pattern.sub(section.rstrip("\n") + "\n", text, count=1)
    else:
        new_text = text.rstrip("\n") + "\n\n" + section

    if new_text == text:
        return False
    data = new_text.encode("utf-8")
    if has_bom:
        data = b"\xef\xbb\xbf" + data
    readme_path.write_bytes(data)
    return True


