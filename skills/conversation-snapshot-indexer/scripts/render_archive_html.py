# --------------------------------------------------------------------------- #
# Minimal, safe Markdown -> HTML (covers what the notes actually use)
# --------------------------------------------------------------------------- #
def render_inline(text: str) -> str:
    """Escape, then apply inline markdown: code, links, bold, italic, bare urls."""
    placeholders: List[str] = []
    sent_l, sent_r = "@@PH", "HP@@"

    def stash(rendered: str) -> str:
        placeholders.append(rendered)
        return f"{sent_l}{len(placeholders) - 1}{sent_r}"

    # inline code first (its contents must not be further processed)
    def code_sub(m: re.Match) -> str:
        return stash(f"<code>{html.escape(m.group(1))}</code>")

    text = re.sub(r"`([^`]+)`", code_sub, text)

    # markdown links [text](url): external opens a new tab, internal stays in the same tab
    def link_sub(m: re.Match) -> str:
        label = html.escape(m.group(1))
        url = m.group(2).strip()
        if re.match(r"^(https?:|mailto:)", url):
            return stash(
                f'<a href="{html.escape(url, quote=True)}" target="_blank" rel="noopener">{label}</a>'
            )
        safe = url if re.match(r"^(/|\.|#)", url) else "#"
        return stash(f'<a href="{html.escape(safe, quote=True)}">{label}</a>')

    text = re.sub(r"\[([^\]]+)\]\(([^)]+)\)", link_sub, text)

    # escape everything still raw
    text = html.escape(text)

    # bold / italic on escaped text
    text = re.sub(r"\*\*([^*]+)\*\*", r"<strong>\1</strong>", text)
    text = re.sub(r"(?<!\*)\*([^*\n]+)\*(?!\*)", r"<em>\1</em>", text)

    # autolink bare urls
    def bare(m: re.Match) -> str:
        u = m.group(0)
        return f'<a href="{u}" target="_blank" rel="noopener">{u}</a>'

    text = re.sub(r"(?<![\"'>=])\bhttps?://[^\s<]+", bare, text)

    # restore placeholders
    for i, rendered in enumerate(placeholders):
        text = text.replace(f"{sent_l}{i}{sent_r}", rendered)
    return text


def render_markdown(md: str) -> str:
    lines = md.splitlines()
    out: List[str] = []
    i = 0
    n = len(lines)

    while i < n:
        line = lines[i]
        stripped = line.strip()

        if not stripped:
            i += 1
            continue

        # heading
        h = re.match(r"^(#{1,6})\s+(.*)$", stripped)
        if h:
            level = len(h.group(1))
            out.append(f"<h{level}>{render_inline(h.group(2).strip())}</h{level}>")
            i += 1
            continue

        # table block
        if stripped.startswith("|") and i + 1 < n and re.match(r"^\|[\s:|-]+\|?$", lines[i + 1].strip()):
            tbl, i = render_table(lines, i)
            out.append(tbl)
            continue

        # Fallback for pipe-prefixed lines that are not valid Markdown table
        # headers. Without consuming the line, the paragraph parser below would
        # see a block-start character and loop forever.
        if stripped.startswith("|"):
            out.append(f"<p>{render_inline(stripped)}</p>")
            i += 1
            continue

        # unordered list block
        if re.match(r"^[-*]\s+", stripped):
            items: List[str] = []
            while i < n and re.match(r"^[-*]\s+", lines[i].strip()):
                items.append(render_inline(re.sub(r"^[-*]\s+", "", lines[i].strip())))
                i += 1
            out.append("<ul>" + "".join(f"<li>{it}</li>" for it in items) + "</ul>")
            continue

        # paragraph (gather until blank / block start)
        para: List[str] = []
        while i < n and lines[i].strip() and not re.match(r"^(#{1,6}\s|[-*]\s|\|)", lines[i].strip()):
            para.append(lines[i].strip())
            i += 1
        out.append(f"<p>{render_inline(' '.join(para))}</p>")

    return "\n".join(out)


def render_table(lines: List[str], start: int) -> Tuple[str, int]:
    header = [c.strip() for c in lines[start].strip().strip("|").split("|")]
    i = start + 2
    rows: List[List[str]] = []
    while i < len(lines) and lines[i].strip().startswith("|"):
        rows.append([c.strip() for c in lines[i].strip().strip("|").split("|")])
        i += 1
    thead = "<tr>" + "".join(f"<th>{render_inline(c)}</th>" for c in header) + "</tr>"
    tbody = "".join(
        "<tr>" + "".join(f"<td>{render_inline(c)}</td>" for c in r) + "</tr>" for r in rows
    )
    return f"<table><thead>{thead}</thead><tbody>{tbody}</tbody></table>", i


