"""Deterministic HTML renderer for the Snapshot Keeper archive.

Reads archive/*.md (human-facing notes produced by the skill) and regenerates,
without any creative writing:

    <root>/index.html                 browsing entry point (search + cards)
    <root>/archive_html/<name>.html   one rendered page per note
    <root>/README.md                  the "## 현재 보관 항목" section only

The skill writes Markdown. HTML is never hand-written: it is regenerated by this
script so the presentation layer stays deterministic and reproducible.

Pure standard library. No third-party dependencies.

Usage:
    python render_archive_html.py
    python render_archive_html.py --archive-root "D:\\_my_tools\\ChatGPT_Snapshot_Archive"
    python render_archive_html.py --no-readme        # skip README rewrite
"""

