def project_root_from_script() -> Path:
    """The skill lives under <project>/skills/...; project-root jobs must not be hidden in the archive root."""
    return Path(__file__).resolve().parents[3]


def count_project_snapshot_jobs(project_root: Path) -> int:
    total = 0
    for month_dir in project_root.iterdir():
        if not month_dir.is_dir() or not PROJECT_MONTH_RE.match(month_dir.name):
            continue
        total += sum(1 for child in month_dir.iterdir() if child.is_dir())
    return total


